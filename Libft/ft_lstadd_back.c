#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*current;

	if (!new)
		return ;
	if (!*lst)
	{
		current = new;
		return ;
	}
	current = ft_lstlast(*lst);
	current->next = new;
}
//when lst is empty then the new should be the first element / or the new